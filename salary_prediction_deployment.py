import streamlit as st
import pandas as pd
import joblib

model = joblib.load("salary_prediction_rfr_model (1).pkl")
encoder = joblib.load("label_encoder_salary.pk1")

st.title("Salary_prediction_model")
age = st.number_input("Enter your age", 18,65)
gender = st.selectbox("select your gender", encoder["Gender"].classes)
education = st.selectbox("select your education", encoder["Education Level"].classes)
job_title = st.selectbox("select your job_title", encoder["Job_Title"].classes)

experience = st.number_input("Enter your experience(in years)",0,50)


df = pd.DataFrame({
    "Age":[age],
    "Gender":["gender"],
    "Education Level":["education"],
    "Job Title":["job_title"],

    "Experience":["experience"]
})

if st.button("predict salary"):
  for col in encoder:
    df[col] = encoder[col].transform(df[col])

    prediction = model.predict(df)
    st.success(f"predicted salary:{prediction[0]}")
